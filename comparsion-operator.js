//17-5 Compare variables and Comparison operator
//**To compare two values, you use a comparison operator. The following table shows the comparison operators in JavaScript:**//
 var num1 = 5;
 var num2 = 6;
 var result1 = (num1 < num2);  //  < less than result : true;
 var result2 = (num1 > num2 ); //  > greater than result : false;
 var result3 = (num1 <= num2); //  <= less than or equal to result : true;
 var result4 = (num1 >= num2); //  >= greater than or equal to result : false;
 var result5 = (num1 == num2); //  == equal to result : false;
 var result6 = (num1 != num2); //  != not equal to result : true;
 
console.log(result1);
console.log(result2);
console.log(result3);
console.log(result4);
console.log(result5);
console.log(result6);






 


